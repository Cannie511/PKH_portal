    private function createPermission() {

        $id = 0;
        foreach ($this->permissions as $item) {
            $item['id'] = ++$id;
            $item["created_at"] = \Carbon\Carbon::now()->toDateTimeString();
            $item["updated_at"] = \Carbon\Carbon::now()->toDateTimeString();

            DB::table('permissions')->insert($item);
        }
    }
    private function createPermissionRole() {
        $id = 1;
        foreach ($this->permissionRole as $permKey => $value) {
            foreach ($value as $roleId) {
                $newItem = [
                    'id' => $id++,
                    'permission_id' => App\Models\Permissions::where('slug', $permKey)->first()->id,
                    'role_id' => $roleId,
                    'created_at' => \Carbon\Carbon::now()->toDateTimeString(),
                    'updated_at' => \Carbon\Carbon::now()->toDateTimeString()
                ];
                DB::table('permission_role')->insert($newItem);
            }
        }
    }
